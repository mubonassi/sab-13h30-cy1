#Lista -> Array

#Uma lista permite você adicionar dados (itens)
#Os itens podem ser qualquer tipo de variavel
lista = ["item1","item2","item3","item4","roberto"]

#Exibindo a lista completa
print(lista)

#Exibir um item especifico da lista
#Index -> o número do item na lista
print(lista[0])
print(f"Item 2: {lista[1]}")